package com.example.mtg_extensions_java_android.Business.Model;

public class Name {
    private String en;

    private String cn;

    private String jp;

    public void setEn(String en) {
        this.en = en;
    }

    public String getEn() {
        return this.en;
    }

    public void setCn(String cn) {
        this.cn = cn;
    }

    public String getCn() {
        return this.cn;
    }

    public void setJp(String jp) {
        this.jp = jp;
    }

    public String getJp() {
        return this.jp;
    }
}
